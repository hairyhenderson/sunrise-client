func sunsetImage() *image.RGBA {
	img := image.NewRGBA(image.Rect(0, 0, 30, len(colors)))
	for y, c := range colors[:] {
		for x := 0; x < 30; x++ {
			// img.Set(x, y, gamma(c, 2.8))
			img.Set(x, y, c)
		}
	}
	return img
}

func getColors() {
	img := sunsetImage()

	f, _ := os.OpenFile("out.png", os.O_WRONLY|os.O_CREATE, 0600)
	defer f.Close()
	png.Encode(f, img)
}

func mustParseHex(hex string) pixel {
	c, err := colorful.Hex(hex)
	if err != nil {
		panic("MustParseHex: " + err.Error())
	}
	return pixel{c}
}

// adjust for gamma
func gamma(c colorful.Color, g float64) colorful.Color {
	n := colorful.Color{
		R: math.Pow(c.R, g),
		G: math.Pow(c.G, g),
		B: math.Pow(c.B, g),
	}
	return n
}

